## Dereu WhatsApp Integration

Этот проект интегрируется с Dereu — WhatsApp CPaaS-хабом поверх Meta Cloud API. Для задач по подключению
номеров (Embedded Signup), приёму вебхуков или отправке сообщений/OTP через Dereu используй скилл
`dereu-integration`.

- Platform key (провижининг): env `DEREU_PLATFORM_KEY`.
- API-ключ компании (отправка): выдаётся per-компания при провижининге, храни в своей БД.
- Полный справочник: https://docs.dereu.noderail.io
